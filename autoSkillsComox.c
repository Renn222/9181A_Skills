void into20() //Getting into the 20 pt zone
{
	resetDriveEncoder();

	while(SensorValue[limitRight] != 1 || SensorValue[limitLeft] != 1)
	{
		motor[frontL] = 125;
		motor[backL] = 125;
		motor[frontR] = 125;
		motor[backR] = 125;
	}
	resetDrive();
	mogo(front);
}


