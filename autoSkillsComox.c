int front = 1;
int back  = -1;

/* Variables used to calculate how fast bot is moving*/
int wheelDiameter = 4.5;
int circumference = wheelDiameter * PI; //Roughly 14.13 inches

int constant = 1;

int distanceMogo1 = (48/circumference)*360; //Distance for the first mogo
int timeMogoIntake = 61; //number of ticks for the mobo intake to go down


